#Integer numeric data type
mobileNo = 9003642465
print('INTEGER DATA TYPE:',mobileNo,type(mobileNo))

temperature = 33.6
print('Float data type',temperature,type(temperature))

complexno = 34j+78
print('Complex :',complexno,type(complexno))


#sequence data type

course = 'python'
print('String data type',course,type(course))

course = ['python','java','php']
print('list data type',course,type(course))

course = ('python','java','php')
print('tuple data type',course,type(course))

#boolean
is_pass = true
print('Boolean data type:',is_pass,type(is_pass))

#set
course = {'python','java','php'}
print('set data type',course,type(course))

#Dictionary
watch_details = {
    'Titan ':4000,
    'Fastrack':5000
}

print('Dictionary Data Type :',watch_details,type(watch_details))
